      <p><b>ANEXO: Artigo Lei Comentada</b></p>

      <p>A reforma trabalhista expressou a resposta legislativa à alteração na mudança da realidade dos trabalhadores,
        configurando uma atualização decorrente de cultura geracional. Compatibilizou-se o texto legal com as
        necessidades
        advindas das inovações tecnológicas, bem como das novas formas de relações de trabalho. Sobretudo, acomodou os
        interesses de trabalhadores e empregadores, propiciando ambiente mais propício à geração de empregos, com o
        objetivo de crescimento do mercado de trabalho e o reconhecimento de novas práticas globais. Questão fundamental
        da mudança é a sobreposição do negociado sobre o legislado. A redação atribuída ao art. 611-A da CLT determina a
        prevalência das normas coletivas (acordos e convenções) sobre os dispositivos legais, quando dispuserem sobre
        temas como jornada de trabalho, intervalo intrajornada, modalidade de registro da jornada, entre outros. O
        objetivo é priorizar o interesse coletivo. As regras de proibição contidas no art. 611B da CLT afastam da ampla
        negociação questões constitucionalmente asseguradas aos trabalhadores, evitando interpretações distorcidas.</p>

      <p>A constitucionalidade da mudança supracitada advém da extração de linha de raciocínio facilmente identificada
        na
        Constituição. A compilação enaltece o trabalhador na medida em que lhe atribui autonomia e legitimidade para
        participar de processos decisórios importantes referentes à sua rotina laborativa, ao mesmo tempo em que
        estimula
        a livre iniciativa, possibilitando o alinhamento entre os interesses e a realidade fática, na dinâmica da
        empresa</p>

      <p>O art. 7º, inciso XXVI, garante aos trabalhadores o reconhecimento dos acordos e convenções coletivas, e
        integra o
        rol de garantias fundamentais dos trabalhadores urbanos e rurais. A Convenção nº 154 da OIT, incorporada à
        legislação nacional, afirma aos sindicatos o direito de decidir e negociar a pauta de reivindicações em
        liberdade,
        sem indesejável interferência do governo. Ademais, os incisos VI, XIII e XIV, ao disporem sobre os direitos
        pertinentes à irredutibilidade salarial, jornada de trabalho não superior a oito horas e jornadas de seis horas
        em
        situações de turnos ininterruptos, prevêm expressamente a ressalva da aplicação dos direitos em caso de
        negociação
        coletiva. Em suma, a Constituição valida as normas coletivas, prestigiando a autonomia da vontade coletiva.</p>

      <p>Importante notar a descaracterização de qualquer hipossuficiência na relação entre os sindicatos
        representativos
        das categorias profissionais e os empregadores. Tratam-se de negociações equilibradas em termos de acesso a
        informações e respaldo técnico. Inclusive, os sindicatos são considerados, de acordo com art. 8º da CF,
        guardiões
        da defesa dos direitos e interesses dos trabalhadores. Ninguém melhor que o próprio sindicato para definir as
        estratégias adequadas a serem adotadas, não havendo, portanto, justificativa plausível para obstar a validade e
        o
        cumprimento dos acordos e convenções, os quais configuram meios legítimos.</p>

      <p>O posicionamento do Supremo Tribunal Federal também é favorável. O ministro Luiz Roberto Barroso, no julgamento
        do
        RE nº 590.415, entendeu válida uma cláusula presente em acordo coletivo que dava quitação geral às verbas
        rescisórias em razão de Plano de Dispensa Incentivada. O ministro elencou como fundamentos o exercício da
        faculdade e da autonomia do empregado em optar (ou não) pelo plano e a não verificação de assimetria de poder no
        âmbito do direito coletivo do trabalho. O então ministro Teori Zavascki também considerou válido o disposto no
        acordo coletivo referente à supressão do pagamento de horas in itinere na situação do RE nº 895.759.</p>

      <p>Deste modo, a sobreposição do negociado sobre o legislado mostra-se compatível com a Constituição, priorizando
        a
        autonomia coletiva da vontade, ensejando o alinhamento do mercado de trabalho à realidade local, prestigiando o
        sindicato de trabalhadores. Acrescente-se que a regra da convenção ou do acordo coletivo tem prazo máximo de
        vigência, admitindo a ampla reflexão quando da renegociação da norma.</p>

      <p>O Sindicato dos Trabalhadores Metalúrgicos, recentemente, apresentou ao Congresso Nacional a proposta de Acordo
        Coletivo de Trabalho com Propósito Específico, denominado à época como ACE, onde o negociado sobre o legislado
        era
        a base da estrutura proposta pelos líderes sindicais.</p>






      <p>Assim, o princípio aprovado pela reforma trabalhista mostra-se decorrente do anseio da sociedade, proposto por
        trabalhadores e empresários, ratificado pelo Congresso Nacional e referendado pelo STF. O negociado sobre o
        legislado, da forma como disposto na reforma trabalhista, é Constitucional.</p>

      <p>A Constituição Federal prevê, no inciso VI, do art. 7º, a possibilidade de redução salarial mediante convenção ou
        acordo coletivo. Portanto, há muito tempo é possível e constitucional o negociado sobre o legislado mediante
        negociação coletiva. Nesse sentido, o legislador ofertou aos sindicatos profissionais e patronais a possibilidade
        de flexibilização da legislação trabalhista dentro de certos parâmetros. Ao possibilitar a redução salarial, ou
        seja, a redução de verba de natureza alimentar, entendo que, excluindo a vida e a segurança física e psíquica do
        trabalhador, o legislador constitucional autorizou que grande margem dos direitos trabalhistas também fossem
        negociados.</p>

      <p>Primeiramente, cumpre esclarecer que tal negociação precisa, necessariamente, ter participação do Sindicato
        Profissional, conforme o art. 8º, inciso VI, da Constituição Federal.</p>

      <p>Necessário atentar para o significado jurídico de negociação e acordo. Tais instrumentos jurídicos não justificam
        renúncias de direitos, mas sim troca de interesses.</p>

      <p>O empregador, que por algum motivo não tem condições financeiras de continuar a conceder determinado benefício
        habitual e previsto na convenção coletiva da categoria, deverá procurar o sindicato dos trabalhadores para a
        negociação coletiva que lhe permita eliminar ou suspender tal benefício e, ao mesmo tempo, respeitar a convenção
        coletiva. Por sua vez, o sindicato profissional, como legítimo representante daqueles trabalhadores, deve obter
        alguma vantagem que compense a perda daquele benefício, respeitando sempre a decisão da assembleia dos
        trabalhadores.</p>

      <p>Antes da reforma trabalhista de 2017 (Lei nº 13.467/2017) cabia ao Poder Judiciário, no caso a Justiça do
        Trabalho, analisar o equilíbrio dessa negociação, evitando uma renúncia de direitos.</p>

      <p>A competência constitucional para a análise de negociações coletivas é fundamental, pelo que dispõe o art. 7º da
        Constituição Federal, razão pela qual qualquer renúncia de direitos deve ser coibida pela Justiça do Trabalho.
        Ocorre que, a partir da Lei nº 13.467/2017, tal competência da Justiça do Trabalho foi reduzida pela redação dos
        §§ 1º e 2º do art. 611-A, da Consolidação das Leis do Trabalho.</p>

      <p>A ressalva prevista no § 3º do art. 611-A, de que a redução salarial só seria possível mediante concessão de
        estabilidade provisória no emprego, demonstra que, em se tratando de direitos trabalhistas, é necessária a
        compensação ou contrapartida no caso de alteração prejudicial de um direito trabalhista, justamente em observância
        ao art. 7º da Constituição Federal.</p>

      <p>Por outro lado, a reforma trabalhista eliminou abruptamente a contribuição sindical e condicionou a contribuição
        assistencial a uma autorização prévia e individual do trabalhador (arts. 578 e 579, da CLT), em desrespeito ao
        inciso IV do art. 8º da Constituição Federal.</p>

      <p>A intenção do legislador ordinário em afastar o sindicato profissional dessa comissão torna-se mais evidente
        quando determina que os documentos do processo eleitoral ficarão “sob a guarda dos empregados e da empresa pelo
        prazo de cinco anos”. (art. 510-D, § 4º, da CLT).</p>

      <p>Todos esses artigos referentes à comissão de representantes dos empregados mostram a relação de acordo com inciso
        III do art. 8º da Constituição Cidadã, que determina ser o sindicato seu legítimo representante.</p>



